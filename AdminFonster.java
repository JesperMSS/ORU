    private void addButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addButtonMouseClicked
        //Samlar in information från textfälten när "lägg till" klickas på med musen
        //Lägger till informationen i databasen 
        if (Validering.textNotEmpty(firstnameField) && Validering.textNotEmpty(surnameField) && Validering.textNotEmpty(emailField) && Validering.textNotEmpty(usernameField) && Validering.textNotEmpty(passwordField)) {
            if (Validering.valideraPassword(passwordField.getText()));
            String firstName = firstnameField.getText();
            String lastName = surnameField.getText();
            String email = emailField.getText();
            String username = usernameField.getText();
            String password = passwordField.getText();
            ArrayList<String> users = null;
            ArrayList<String> emails = null;
            try {
                emails = idb.fetchColumn("SELECT email FROM USERS");
                users = idb.fetchColumn("SELECT USERNAME from users");

            } catch (InfException ex) {
                Logger.getLogger(AdminFonster.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (Validering.isUnique(username, users)) {
                if (Validering.isUnique(email, emails)) {
                    try {
                        String fraga2 = "INSERT INTO USERS (förnamn, efternamn, email, password, username, Admin) VALUES ('" + firstName + " ', '" + lastName + "', '" + email + "', '" + password + "','" + username + "', 'n');";
                        idb.insert(fraga2);
                        JOptionPane.showMessageDialog(null, "Användare har lagts till");
                    } catch (InfException ex) {
                        errorMsg.setText("Databas fel");
                    } catch (NullPointerException ex) {
                        errorMsg.setText("Något gick fel");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Email finns redan, välj en ny");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Username finns redan, välj ett nytt");
            }
        }
    }//GEN-LAST:event_addButtonMouseClicked

    private void removeUserButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeUserButtonMouseClicked
        //Hämtar objektet som är valt i combo boxen och 
        //tar bort det från databasen när man klickar på "ta bort" knappen
        String userToBeDeleted = userCombo.getSelectedItem().toString();
        String deleteQuery = "DELETE FROM users WHERE förnamn = '" + userToBeDeleted + "';";

        try {
            idb.delete(deleteQuery);
            JOptionPane.showMessageDialog(null, "Användaren togs bort");

        } catch (InfException ex) {
            Logger.getLogger(Calender.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_removeUserButtonMouseClicked

