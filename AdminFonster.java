    private void fillCombo() {
        //Fyller comboboxen med alla users som inte är admin 
        //Metoden kör i constructorn
        String usersQuery = "SELECT förnamn FROM USERS WHERE Admin NOT LIKE 'J'";
        ArrayList<String> userList;
        try {
            userList = idb.fetchColumn(usersQuery);
            for (String user : userList) {
                userCombo.addItem(user);
            }
        } catch (InfException ex) {
            Logger.getLogger(Calender.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

