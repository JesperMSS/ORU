    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //If-sats som kontrollerar om användare klickar i "Ja" eller "Nej" och uppdaterar status i databasen.
        //getSelectedIndex hämtar valt möte i Comboboxen.
        try {
            int indexSelected = jComboBox1.getSelectedIndex();
            String getMeetingId = users.get(indexSelected--);

            if (jRadioButton1.isSelected()) {
                String fråga = "update förfrågningar set kommer = 'J' where meeting_ID = " + getMeetingId + " and userID =" + userID;
                String fråga2 = "insert into at_meeting (userID, meeting_ID) values ( " + userID + "," + getMeetingId +")";

                idb.update(fråga);
                idb.insert(fråga2);
                JOptionPane.showMessageDialog(null, "Svar registrerat");
            }
            if (jRadioButton2.isSelected()) {
                String fråga2 = "update förfrågningar set kommer = 'N' where meeting_ID = " + getMeetingId + " and userID =" + userID;
                idb.update(fråga2);

                JOptionPane.showMessageDialog(null, "Svar registrerat");
            }
        } catch (InfException ex) {
            Logger.getLogger(FörfrågningarFrånAnställda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void fyllCombo() {
        //Hämtar möter från databasen och fyller Comboboxen. 
        try {

            String fråga2 = "Select meeting_ID FROM förfrågningar WHERE userID = '" + userID + "' AND kommer LIKE ('q');";
            users = idb.fetchColumn(fråga2);

            int nummer = 1;
            for (String user : users) {
                jComboBox1.addItem("Möte" + nummer++ + "\n");
            }

        } catch (InfException ex) {
            Logger.getLogger(FörfrågningarFrånAnställda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

