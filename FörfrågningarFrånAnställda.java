    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // Hämtar datum och stoppar in i tabellen
        try {
            int getMotesIndex = jComboBox1.getSelectedIndex();
            String getMotesID = users.get(getMotesIndex--);

            String fragaDatum = "SELECT Datum FROM meeting WHERE meeting_ID = " + getMotesID + ";";
            String fragaStartTid = "SELECT Start_Time FROM meeting WHERE meeting_ID = " + getMotesID + ";";
            String fragaSlutTid = "SELECT End_Time FROM meeting WHERE meeting_ID = " + getMotesID + ";";
            String fragaInfo = "SELECT info FROM meeting WHERE meeting_ID = " + getMotesID + ";";

            jDatumField.setText(idb.fetchSingle(fragaDatum));
            jTimeStart.setText(idb.fetchSingle(fragaStartTid));
            jTimeEnd.setText(idb.fetchSingle(fragaSlutTid));
            jInfoField.setText(idb.fetchSingle(fragaInfo));

        } catch (InfException ex) {
            Logger.getLogger(FörfrågningarFrånAnställda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

